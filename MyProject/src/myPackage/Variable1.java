package myPackage;

public class Variable1 {

	public static void main(String[] args) {
		
		int a=5; // integer type local variable defination 	
		System.out.println(a);
		

		int x=100; // integer type local variable variable 	
		System.out.println(x); //100
		

		x=200; // integer type local variable variable 	
		System.out.println(x);
		
		char c ='a'; //char type local variable, should be placed inside single quote
		System.out.println(c); //a
 		
		boolean b = true;
		System.out.println(b); //true
		
		System.out.println(3>12); // false

	}

}
